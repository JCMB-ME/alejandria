export const t = derived(language, ($lang) => {
  return (key: keyof typeof translations['en'], params?: Record<string, string | number>) => {
    let text = translations[$lang]?.[key] || translations['en']?.[key] || String(key);
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  };
});

export function translateShelfName(name: string, lang: Language): string {
  if (name === 'Currently Reading') return lang === 'es' ? 'Leyendo actualmente' : 'Currently Reading';
  if (name === 'Finished') return lang === 'es' ? 'Libros terminados' : 'Finished';
  if (name === 'Wishlist') return lang === 'es' ? 'Lista de deseos' : 'Wishlist';
  if (name === 'Favorites') return lang === 'es' ? 'Favoritos' : 'Favorites';
  return name;
}

export function setLanguage(lang: Language) {
  language.set(lang);
  if (browser) {
    localStorage.setItem('alejandria-lang', lang);
  }
}

export function initLanguage() {
  if (!browser) return;
  const saved = localStorage.getItem('alejandria-lang') as Language | null;
  if (saved && (saved === 'en' || saved === 'es')) {
    language.set(saved);
  } else {
    const navLang = navigator.language.split('-')[0];
    if (navLang === 'es') {
      language.set('es');
    } else {
      language.set('en');
    }
  }
}
